import { writeFile } from 'node:fs/promises';

import Ajv from 'ajv';
import { _ } from 'ajv/dist/compile/codegen/index.js';
import standaloneCode from 'ajv/dist/standalone/index.js';

import {
  commandSchemas,
  entityCatalogSchema,
  fidelityCatalogSchema,
  lessonMetadataSchema,
  sceneDirectiveSchema,
} from '../src/lesson/schema-definitions.js';
import httpsFormats from '../src/lesson/https-formats.js';

const schemas = {
  lessonMetadata: lessonMetadataSchema,
  sceneDirective: sceneDirectiveSchema,
  entityCatalog: entityCatalogSchema,
  fidelityCatalog: fidelityCatalogSchema,
  commandSceneReplace: commandSchemas['scene.replace'],
  commandCameraSet: commandSchemas['camera.set'],
  commandVisibilitySet: commandSchemas['visibility.set'],
  commandHemispheresSetGlobal: commandSchemas['hemispheres.set-global'],
  commandHemispheresSetEntity: commandSchemas['hemispheres.set-entity'],
  commandCutawaySet: commandSchemas['cutaway.set'],
  commandMaterialSet: commandSchemas['material.set'],
  commandPlaybackSet: commandSchemas['playback.set'],
  commandSelectionSet: commandSchemas['selection.set'],
  commandVisualSet: commandSchemas['visual.set'],
  commandControlsSet: commandSchemas['controls.set'],
};

const ajv = new Ajv({
  allErrors: true,
  strict: true,
  code: { source: true, esm: true, lines: true, formats: _`httpsFormats` },
});
ajv.addFormat('https-url', httpsFormats['https-url']);

const exportIds = {};
for (const [name, schema] of Object.entries(schemas)) {
  const id = `brain-atlas:${name}`;
  ajv.addSchema({ ...schema, $id: id }, id);
  exportIds[name] = id;
}

const generated = standaloneCode(ajv, exportIds)
  .replaceAll('require("ajv/dist/runtime/ucs2length").default', 'ucs2Runtime.default')
  .replaceAll('require("ajv/dist/runtime/equal").default', 'equalRuntime.default');
const banner = `// Generated by scripts/generate-lesson-validators.mjs. Do not edit.\n` +
  `// CSP-safe: schemas compile during development, not in the browser.\n` +
  `import ucs2Runtime from 'ajv/dist/runtime/ucs2length.js';\n` +
  `import equalRuntime from 'ajv/dist/runtime/equal.js';\n` +
  `import httpsFormats from './https-formats.js';\n`;
await writeFile(
  new URL('../src/lesson/generated-validators.js', import.meta.url),
  `${banner}${generated}`.trimEnd() + '\n',
);
